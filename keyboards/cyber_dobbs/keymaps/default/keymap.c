#ifdef OLED_ENABLE

// Rotate OLED
oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_180;  
}

// Draw to OLED
bool oled_task_user() {
    // Set cursor position
    oled_set_cursor(0, 1);
    
    // Write text to OLED
    uint8_t current_layer = get_highest_layer(layer_state);
    
    // Convert layer number to a string label
    switch (current_layer) {
        case 0:
            oled_write_ln("Profile 1", false);
            break;
        case 1:
            oled_write_ln("Profile 2", false);
            break;
        case 2:
            oled_write_ln("Profile 3", false);
            break;
        case 3:
            oled_write_ln("Profile 4", false);
            break;
        default:
            // For any additional layers
            oled_write_P(PSTR("Profile "), false);
    }

    
    return false;
}

#endif

bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { /* First encoder */
        if (clockwise) {
            tap_code(KC_A);
        } else {
            tap_code(KC_B);
        }
    }
    return false;
}

